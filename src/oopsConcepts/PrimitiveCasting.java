package oopsConcepts;

public class PrimitiveCasting 
{
	public static void main(String[] args)
	{
		
		int a = 100;
		
		float b = /*(float)*/ a;   
		
		System.out.println(a);
		System.out.println(b);
		
		float c = 99.93f;
		int d = (int) c;
		
		
		System.out.println(c);
		System.out.println(d);
		
	}
}
